public class Word {
    
    private String word;
    private String type;
    private int state;
    
    public Word(String word, String type, int state) {
        this.word = word;
        this.type = type;
        this.state = state;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }
    
}
